import { Switch, Route } from "wouter";
import { queryClient } from "./lib/queryClient";
import { QueryClientProvider } from "@tanstack/react-query";
import { Toaster } from "./components/ui/toaster.txt"; // Changed @/ to ./
import { TooltipProvider } from "./components/ui/tooltip"; // Changed @/ to ./
import Dashboard from "./pages/Dashboard"; // Changed @/ to ./
import NotFound from "./pages/not-found"; // Changed @/ to ./

function Router() {
  return (
    <Switch>
      <Route path="/" component={Dashboard} />
      <Route component={NotFound} />
    </Switch>
  );
}

function App() {
  return (
    <QueryClientProvider client={queryClient}>
      <TooltipProvider>
        <Router />
        <Toaster />
      </TooltipProvider>
    </QueryClientProvider>
  );
}

export default App;
